// Evaluar el desempeño de empleado segun su puntaje
let puntaje = 99;

if(puntaje >= 90){
    console.log("Desempeño excelente");
} else if(puntaje >= 75){
    console.log("Desempeño bueno")
}else if(puntaje >= 60){
    console.log("Desempeño aceptable")
} else {
    console.log("Desempeño insuficiente")
}